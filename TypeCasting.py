# Typecasting = the process of converting a variable from one data type to another
# str(), int(), float(), bool()

name = "Arganata"
age = 21
gpa = 3.2
is_student = True

name = str(name)
print(name)

age = int(age)
print(age)

gpa = float(gpa)
print(gpa)

is_student = bool(is_student)
print(is_student)
